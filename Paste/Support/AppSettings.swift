    // MARK: - Filter Tab Names

    private static let filterTabNamePrefix = "filterTabName_"

    static func filterTabName(for type: ClipboardItemType?) -> String? {
        let suffix: String
        switch type {
        case nil:     suffix = "all"
        case .text:   suffix = "text"
        case .image:  suffix = "image"
        case .file:   suffix = "file"
        case .some:   suffix = "unknown"
        }
        return defaults.string(forKey: "\(filterTabNamePrefix)\(suffix)")
    }

    static func setFilterTabName(_ name: String, for type: ClipboardItemType?) {
        let suffix: String
        switch type {
        case nil:     suffix = "all"
        case .text:   suffix = "text"
        case .image:  suffix = "image"
        case .file:   suffix = "file"
        case .some:   suffix = "unknown"
        }
        let key = "\(filterTabNamePrefix)\(suffix)"
        let trimmed = name.trimmingCharacters(in: .whitespacesAndNewlines)
        if trimmed.isEmpty {
            defaults.removeObject(forKey: key)
        } else {
            defaults.set(trimmed, forKey: key)
        }
    }

    // MARK: - Pinboard
    
    static let pinboardCountMax = 10
    static let pinboardCountDefault = 5

    static var pinboardCount: Int {
        get { max(pinboardCountDefault, min(int(Key.pinboardCount, default: pinboardCountDefault), pinboardCountMax)) }
        set { defaults.set(max(pinboardCountDefault, min(newValue, pinboardCountMax)), forKey: Key.pinboardCount) }
    }

    static var lastPinboardIndex: Int {
        get {
            let value = int(Key.lastPinboardIndex, default: 0)
            return max(0, min(value, pinboardCount - 1))
        }
        set {
            defaults.set(max(0, min(newValue, pinboardCount - 1)), forKey: Key.lastPinboardIndex)
        }
    }

