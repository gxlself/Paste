    // MARK: - Panel Position

    enum PanelPosition: Int, CaseIterable {
        case bottom = 0
        case top = 1
        case left = 2
        case right = 3
    }

    static var panelPosition: PanelPosition {
        get {
            PanelPosition(rawValue: int(Key.panelPosition, default: PanelPosition.bottom.rawValue)) ?? .bottom
        }
        set { defaults.set(newValue.rawValue, forKey: Key.panelPosition) }
    }

    // MARK: - Appearance

    enum Appearance: Int, CaseIterable {
        case system = 0
        case light = 1
        case dark = 2
    }

    // MARK: - Helpers

    private static let defaults = UserDefaults.standard

    private static func bool(_ key: String, default defaultValue: Bool) -> Bool {
        if defaults.object(forKey: key) == nil { return defaultValue }
        return defaults.bool(forKey: key)
    }

    private static func int(_ key: String, default defaultValue: Int) -> Int {
        defaults.object(forKey: key) as? Int ?? defaultValue
    }

    private static func uint32(_ key: String, default defaultValue: UInt32) -> UInt32 {
        defaults.object(forKey: key) as? UInt32 ?? defaultValue
    }

    private static func modifierFlags(_ key: String, default defaultValue: NSEvent.ModifierFlags) -> NSEvent.ModifierFlags {
        let raw = defaults.object(forKey: key) as? UInt ?? defaultValue.rawValue
        return NSEvent.ModifierFlags(rawValue: raw)
    }

