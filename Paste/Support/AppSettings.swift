    // MARK: - Custom Types (iCloud KV Store, syncs with iOS)

    private static let customTypesKVKey = "customTypes"
    private static let kvStore = NSUbiquitousKeyValueStore.default

    /// Loads the current custom type list from iCloud KV Store.
    static var customTypes: [CustomType] {
        get {
            guard let data = kvStore.data(forKey: customTypesKVKey) else { return [] }
            return (try? JSONDecoder().decode([CustomType].self, from: data)) ?? []
        }
        set {
            if let data = try? JSONEncoder().encode(newValue) {
                kvStore.set(data, forKey: customTypesKVKey)
                kvStore.synchronize()
            }
        }
    }

    /// Creates a new custom type with the given name and returns it.
    @discardableResult
    static func addCustomType(name: String) -> CustomType {
        let trimmed = name.trimmingCharacters(in: .whitespacesAndNewlines)
        let newType = CustomType(name: trimmed.isEmpty ? "Custom" : trimmed)
        var list = customTypes
        list.append(newType)
        customTypes = list
        return newType
    }

    /// Removes the custom type with the given id.
    static func removeCustomType(id: String) {
        customTypes = customTypes.filter { $0.id != id }
    }

    /// Renames the custom type with the given id.
    static func renameCustomType(id: String, name: String) {
        var list = customTypes
        guard let idx = list.firstIndex(where: { $0.id == id }) else { return }
        list[idx].name = name.trimmingCharacters(in: .whitespacesAndNewlines)
        customTypes = list
    }
